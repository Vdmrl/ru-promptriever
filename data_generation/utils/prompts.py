# Helper functions

def get_random_generation_params() -> dict[str, str]:
    """
    Returns random parameters for instruction generation.
    Selects length first, then matches with appropriate style length.
    """
    length_idx = random.randrange(len(LENGTH_OPTIONS))

    # Use short styles for the first two length options, long for the rest
    style_pool = STYLE_OPTIONS_SHORT if length_idx < 2 else STYLE_OPTIONS_LONG

    return {
        "style_instruction": random.choice(style_pool),
        "length_format": LENGTH_OPTIONS[length_idx]
    }
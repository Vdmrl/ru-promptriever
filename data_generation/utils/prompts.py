# Pydantic models
class InstructionGenOutput(BaseModel):
    # Stage 1 (paraphrasing)
    rewritten_query: str = Field(
        description="Переписанный запрос: естественный, грамотный русский язык, без следов машинного перевода."
    )

    rewritten_pos_doc: str = Field(
        description="Переписанный первый документ: связный текст, логически реконструированный смысл."
    )

    rewritten_pos_title: str = Field(
        description="Заголовок для первого документа (сгенерированный на основе переписанного текста)."
    )

    rewritten_neg_doc: str = Field(
        description="Переписанный второй документ: связный текст, логически реконструированный смысл."
    )

    rewritten_neg_title: str = Field(
        description="Заголовок для второго документа (сгенерированный на основе переписанного текста)."
    )

    # Stage 2 (generation)
    reasoning: str = Field(
        description="Кратко опиши логику: как именно ты сформулируешь запрос от лица пользователя, используя конкретные аспекты (факты, контекст, тональность, отсутствие деталей), чтобы отсечь ненужный документ?"
    )
    instruction: str = Field(
        description="Текст сложного поискового запроса от лица пользователя. Чтобы достичь нужной длины, подробно описывай контекст ситуации и специфические требования к контенту. КАТЕГОРИЧЕСКИ ЗАПРЕЩЕНО задавать вопросы и использовать фразы ассистента: 'Уточните...', 'Пожалуйста...', 'Я помогу...'. Текст должен быть декларативным."
    )
    relevant_docs: list[int] = Field(
        description="Список с ID ЕДИНСТВЕННОГО документа, который остался релевантным."
    )
    non_relevant_docs: list[int] = Field(
        description="Список ID всех остальных документов."
    )


class InstructionNegativeEntry(BaseModel):
    matches_both: bool = Field(
        description="True, если пассаж подходит и под запрос, и под инструкцию. False, если только под запрос (Instruction Negative)."
    )
    error_type: Literal["none", "different_interpretation", "omission", "mention_non_relevant_flag"] = Field(
        description="Тип ошибки. Строго по статье: 'none' (идеал), 'different_interpretation' (другая трактовка), 'omission' (упущение), 'mention_non_relevant_flag' (упоминание запрещенного)."
    )
    explanation: str = Field(
        description="Краткое пояснение причины выбора типа ошибки."
    )
    title: str = Field(description="Заголовок документа.")
    passage: str = Field(description="Текст пассажа (около 100 слов).")


class InstructionNegativeMiningOutput(BaseModel):
    reasoning: str = Field(
        description="План генерации: как именно ты исказишь смысл для создания 3-х разных негативов (interpretation, omission, flag)?"
    )
    documents: list[InstructionNegativeEntry] = Field(
        description="Список сгенерированных документов (должно быть ровно 4 документа: 3 негативных и 1 позитивный)."
    )


